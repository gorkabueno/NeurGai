package com.ehu.neurgai;

public class Tarifa {
	
	private int hora;
	
	private String fecha;
	private Double feu;

	public int getHora() {
		return hora;
	}
	public void setHora(int hora) {
		this.hora = hora;
	}
	public Double getFeu() {
		return feu;
	}
	public void setFeu(Double feu) {
		this.feu = feu;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
}
